package sampleproject;

public class Switch {
	public static void main(String[] args)
	{
	char b='e';
	switch(b)
	{
		case 'a':
			System.out.println("It is a vowel");
			break;
		case 'b':
		System.out.println("Is not a vowel");
		break;
		case 'e':
			System.out.println("Is a vowel");
			break;
			default:
				System.out.println("not a vowel");
				break;
			
			
	}
	}
}


